				<el-select v-model="ruleForm.shangpinleixing" placeholder="请选择商品类型" :disabled=" false  ||ro.shangpinleixing" >
					<el-option
						v-for="(item,index) in shangpinleixingOptions"
						:key="index"
						:label="item"
						:value="item">
					</el-option>
				</el-select>
